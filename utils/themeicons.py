# A simple script for searching for the provided app's name in the themes icon folder *first*
#
from ignis import utils as Utils
import os


theme = "nixie"  # This is an assumption that should be edited later!

def get_theme_icon(app_name):
    theme_path = '/'.join(Utils.get_current_dir().split("/")[:-1]) + "/themes/" + theme + "/apps"

    apps = next(os.walk(theme_path))[2]
    apps_names = ['.'.join(app.split(".")[:-1]).lower() for app in apps]
    if not app_name.lower() in apps_names:
        return

    return theme_path + "/" + apps[apps_names.index(app_name.lower())]
